package org.example;

public class calculator {
    public static int sum(int a, int b)
    {
        return a + b;
    }
    public static int subtract(int a, int b)
    {
        return a - b;
    }
    public static int multiply(int a, int b)
    {
        return a * b;
    }
    public static double division(int a, int b)
    {
        return a / b;
    }
}
